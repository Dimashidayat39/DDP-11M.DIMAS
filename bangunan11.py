def luas_segitiga(alas, tinggi):
    luas = 0.5 * alas * tinggi
    print("luas segitiga dengan alas", alas, "dan tinggi", tinggi, "=", luas)

def keliling_segitiga(sisi1, sisi2, sisi3):
    keliling = sisi1 + sisi2 + sisi3
    print("keliling segitiga dengan sisi", sisi1, ",", sisi2, ", dan", sisi3, "=", keliling)

def luas_persegi(sisi):
    luas = sisi * sisi
    print("luas persegi dengan sisi", sisi, "=", luas)

def keliling_persegi(sisi):
    keliling = 4 * sisi
    print("keliling persegi dengan sisi", sisi, "=", keliling)

def luas_persegi_panjang(panjang, lebar):
    luas = panjang * lebar
    print("luas persegi panjang dengan panjang", panjang, "dan lebar", lebar, "=", luas)

def keliling_persegi_panjang(panjang, lebar):
    keliling = 2 * (panjang + lebar)
    print("keliling persegi panjang dengan panjang", panjang, "dan lebar", lebar, "=", keliling)

def luas_belah_ketupat(diagonal1, diagonal2):
    luas = 0.5 * diagonal1 * diagonal2
    print("luas belah ketupat dengan diagonal1", diagonal1, "dan diagonal2", diagonal2, "=", luas)

def keliling_belah_ketupat(sisi):
    keliling = 4 * sisi
    print("keliling belah ketupat dengan sisi", sisi, "=", keliling)

def luas_jajar_genjang(alas, tinggi):
    luas = alas * tinggi
    print("luas jajar genjang dengan alas", alas, "dan tinggi", tinggi, "=", luas)

def keliling_jajar_genjang(sisi1, sisi2):
    keliling = 2 * (sisi1 + sisi2)
    print("keliling jajar genjang dengan sisi1", sisi1, "dan sisi2", sisi2, "=", keliling)